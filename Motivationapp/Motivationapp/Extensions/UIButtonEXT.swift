import UIKit

extension UIButton {
    func setSelectedColor(){
        self.backgroundColor = #colorLiteral(red: 0, green: 0.8158839345, blue: 0, alpha: 1)
    }
    func setDeselectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.5425800681, green: 0.9836143851, blue: 0, alpha: 1)
    }
}
